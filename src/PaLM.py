from langchain.llms import GooglePalm

def get_PaLM_result(prompt, google_api_key) -> str:
    """Generate a summary for the given prompt using PaLM.\n
    
    Keyword arguments:
    prompt -- prompt to be summarized\n
    google_api_key -- Google API key to be used for PaLM\n
    Return: Summary generated by PaLM
    """
    
    llm = GooglePalm(google_api_key=google_api_key)
    llm.temperature = 0
    
    prompts = [prompt]
    llm_result = llm.generate(prompts=prompts)
    
    return llm_result.generations[0][0].text


